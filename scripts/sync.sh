      at localhost:9042 — install Ferrosa first:
        brew install ferrosadb/tap/ferrosa

      Register with Claude Code by adding to your MCP config:
        {
          "mcpServers": {
            "ferrosa-memory": {
              "command": "#{bin}/ferrosa-memory-mcp",
