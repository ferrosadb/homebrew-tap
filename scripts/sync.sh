  end

  test do
    assert_match "ferrosa-memory", shell_output("#{bin}/ferrosa-memory-mcp --version")
  end
end
RB
  echo "wrote Formula/ferrosa-memory.rb (${tag})"
}

write_ferrosa
write_ferrosa_memory
echo "done."
